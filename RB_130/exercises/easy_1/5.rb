def rot13(str)
  str.chars.map do |let|
    decrypt(let)
  end
  .join
end

def decrypt(let)
  let = let.downcase
  case let
  when 'a'..'m' then (let.ord + 13).chr
  when 'n'..'z' then (let.ord - 13).chr
  else let
  end
end

